const btnGenerate = document.getElementById("generate");
const selRegion = document.getElementById("region");
const hDriver = document.getElementById("driver");
const hPlate = document.getElementById("plate");

btnGenerate.addEventListener("click", generate);
selRegion.addEventListener("change", generate);

function generate() {
  const driver = generateDriver();
  const plate = generatePlate(selRegion.value);
  hDriver.innerText = driver;
  hPlate.innerText = plate;
}

function generateDriver() {
  const fname = firstnames[randInt(0, firstnames.length - 1)];
  const lname = lastnames[randInt(0, lastnames.length - 1)];
  return `${fname.toUpperCase()} ${lname.toUpperCase()}`;
}

function generatePlate(region) {
  let plate = region;
  for (let i = 0; i < 2; i++) {
    const code = randInt(65, 90);
    plate += String.fromCharCode(code);
  }
  const digits = randInt(100, 999);
  return `${plate} ${digits}`;
}

function randInt(min, max) {
  return Math.floor(min + Math.random() * (max - min + 1));
}

generate();
